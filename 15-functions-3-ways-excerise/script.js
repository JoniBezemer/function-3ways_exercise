//function expression

const functionExpression = function (numberA, numberB){
    return(numberA + numberB)
};

console.log(functionExpression(3, 6))

// function declaration

function functionDeclaration(number1, number2) {
    return number1 + number2;
  }
  
  console.log(functionDeclaration(3, 6)); 

//arrow function

const functionArrow = (num1, num2) => {
  return num1 + num2;
};

console.log(functionArrow(3, 6));

  